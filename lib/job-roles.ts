// Detect role category from description
export function detectRoleCategory(role: string): string {
  const roleLower = role.toLowerCase()
  
  // Tech roles
  if (roleLower.includes('frontend') || roleLower.includes('react') || roleLower.includes('vue') || roleLower.includes('angular')) {
    return 'frontend'
  }
  if (roleLower.includes('backend') || roleLower.includes('node') || roleLower.includes('python') || roleLower.includes('java') || roleLower.includes('go')) {
    return 'backend'
  }
  if (roleLower.includes('full stack') || roleLower.includes('fullstack')) {
    return 'fullstack'
  }
  if (roleLower.includes('mobile') || roleLower.includes('ios') || roleLower.includes('android') || roleLower.includes('react native')) {
    return 'mobile'
  }
  if (roleLower.includes('data engineer') || roleLower.includes('data scientist')) {
    return 'data'
  }
  if (roleLower.includes('machine learning') || roleLower.includes('ml engineer') || roleLower.includes('ai')) {
    return 'ml'
  }
  if (roleLower.includes('devops') || roleLower.includes('sre') || roleLower.includes('cloud') || roleLower.includes('infrastructure')) {
    return 'devops'
  }
  if (roleLower.includes('security')) {
    return 'security'
  }
  if (roleLower.includes('lead') || roleLower.includes('manager') || roleLower.includes('director') || roleLower.includes('vp') || roleLower.includes('cto') || roleLower.includes('ceo') || roleLower.includes('cfo') || roleLower.includes('coo')) {
    return 'leadership'
  }
  
  // Non-Tech roles
  if (roleLower.includes('sales') || roleLower.includes('account executive') || roleLower.includes('business development') || roleLower.includes('bdr') || roleLower.includes('account manager')) {
    return 'sales'
  }
  if (roleLower.includes('marketing') || roleLower.includes('growth') || roleLower.includes('seo') || roleLower.includes('content') || roleLower.includes('brand')) {
    return 'marketing'
  }
  if (roleLower.includes('finance') || roleLower.includes('accounting') || roleLower.includes('accountant') || roleLower.includes('financial') || roleLower.includes('controller') || roleLower.includes('treasurer')) {
    return 'finance'
  }
  if (roleLower.includes('hr ') || roleLower.includes('human resource') || roleLower.includes('recruiter') || roleLower.includes('talent') || roleLower.includes('people operations')) {
    return 'hr'
  }
  if (roleLower.includes('operations') || roleLower.includes('supply chain') || roleLower.includes('logistics') || roleLower.includes('procurement') || roleLower.includes('warehouse')) {
    return 'operations'
  }
  if (roleLower.includes('customer service') || roleLower.includes('customer support') || roleLower.includes('customer success') || roleLower.includes('support specialist') || roleLower.includes('help desk')) {
    return 'customerservice'
  }
  if (roleLower.includes('nurse') || roleLower.includes('doctor') || roleLower.includes('physician') || roleLower.includes('medical') || roleLower.includes('healthcare') || roleLower.includes('clinical') || roleLower.includes('pharmacist') || roleLower.includes('therapist')) {
    return 'healthcare'
  }
  if (roleLower.includes('teacher') || roleLower.includes('professor') || roleLower.includes('instructor') || roleLower.includes('education') || roleLower.includes('academic') || roleLower.includes('trainer') || roleLower.includes('principal')) {
    return 'education'
  }
  if (roleLower.includes('attorney') || roleLower.includes('lawyer') || roleLower.includes('legal') || roleLower.includes('counsel') || roleLower.includes('paralegal') || roleLower.includes('compliance officer')) {
    return 'legal'
  }
  if (roleLower.includes('designer') || roleLower.includes('creative') || roleLower.includes('art director') || roleLower.includes('copywriter') || roleLower.includes('illustrator') || roleLower.includes('photographer')) {
    return 'creative'
  }
  if (roleLower.includes('hotel') || roleLower.includes('restaurant') || roleLower.includes('hospitality') || roleLower.includes('chef') || roleLower.includes('catering') || roleLower.includes('front desk')) {
    return 'hospitality'
  }
  if (roleLower.includes('retail') || roleLower.includes('store manager') || roleLower.includes('merchandise') || roleLower.includes('sales associate')) {
    return 'retail'
  }
  
  // Default to behavioral for unmatched roles
  return 'leadership'
}

